void navegacaoMenuProduto(void){
  char opcao;
  do{
    opcao = menuProduto();
    switch(opcao){
      case '1':
        telaCadastroProduto();
        telaConfirmacao();
        break;
      case '2':
        telaPesquisarProduto();
        break;
      case '3':
        telaApagarProduto();
        telaConfirmacao();
        break;
      case '4':
        telaAtualizarProduto();
        //Cuidado com o tipo da variável
        telaAddValor();
        telaConfirmacao();
        break;
    }
  }while(opcao!='0');
}